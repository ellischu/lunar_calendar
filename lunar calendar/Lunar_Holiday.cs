        /// <summary>
        /// 計算國曆當天對應的節氣 ，""表示不是節氣
        /// </summary>
        /// <returns>節氣名稱，""表示不是節氣</returns>
        public string GetLunarHolDay(DateTime date1)
        {
            const ushort START_YEAR = 1901;
            const ushort END_YEAR = 2050;

            byte Flag;
            int Day, iYear, iMonth, iDay;
            iYear = date1.Year;
            if ((iYear < 1901) || (iYear > END_YEAR))
            {
                return "";
            }

            iMonth = date1.Month;
            iDay = date1.Day;
            Flag = gLunarHoliDay[((iYear - START_YEAR) * 12) + iMonth - 1];

            if (iDay < 15)
            {
                Day = 15 - ((Flag >> 4) & 0x0f);
            }
            else
            {
                Day = (Flag & 0x0f) + 15;
            }

            if (iDay == Day)
            {
                if (iDay > 15)
                {
                    return LunarHolDayName[(iMonth - 1) * 2 + 1];
                }
                else
                {
                    return LunarHolDayName[(iMonth - 1) * 2];
                }
            }
            else
            {
                return "";
            }

        }//function GetLunarHolDay(DateTime date1)

        /// <summary>
        /// 定氣法計算二十四節氣,二十四節氣是按地球公轉來計算的，並非是陰曆計算的
        /// 節氣的定法有兩種。古代曆法採用的稱為"恒氣"，即按時間把一年等分為24份，
        /// 每一節氣平均得15天有餘，所以又稱"平氣"。現代農曆採用的稱為"定氣"，即
        /// 按地球在軌道上的位置為標準，一周360°，兩節氣之間相隔15°。由於冬至時地
        /// 球位於近日點附近，運動速度較快，因而太陽在黃道上移動15°的時間不到15天。
        /// 夏至前後的情況正好相反，太陽在黃道上移動較慢，一個節氣達16天之多。採用
        /// 定氣時可以保證春、秋兩分必然在晝夜平分的那兩天。
        /// </summary>
        /// <param name="date1">日期</param>
        /// <returns>節氣名稱</returns>
        public string ChineseTwentyFourDay(DateTime date1)
        {
            int[] sTermInfo = new int[] {
                0       , 21208 , 42467 , 63836 , 85337 , 107014,
                128867  , 150921, 173149, 195551, 218072, 240693,
                263343  , 285989, 308563, 331033, 353350, 375494, 
                397447  , 419210, 440795, 462224, 483532, 504758 };
            DateTime baseDateAndTime = new(1900, 1, 6, 2, 5, 0); //#1/6/1900 2:05:00 AM#
            string tempStr = "";

            for (int i = 1; i <= 24; i++)
            {

                DateTime newDate = baseDateAndTime.AddMinutes(525948.76 * (date1.Year - 1900) + sTermInfo[i - 1]);
                if (newDate.DayOfYear == date1.DayOfYear)
                {
                    tempStr = LunarHolDayName[i - 1];
                    break;
                }
            }
            return tempStr;
        }//function ChineseTwentyFourDay(DateTime date1)

